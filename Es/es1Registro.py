#inserire una letterea dell'alfabeto da tastiera e detreminare se è una vocale o una consonante 
lettera = (input("inserisci una lettera: ")).lower

if (lettera) == 'a' : 
    print(f'{lettera} è una vocale')
elif (lettera) == 'e' :
    print(f"{lettera} è una vocale")
elif (lettera) == 'i' :
    print(f"{lettera} è una vocale")
elif (lettera) == 'o' :
    print(f"{lettera} è una vocale")
elif (lettera) == 'u' :
    print(f"{lettera} è una vocale")
else : 
    print(f"{lettera} è una consonante")


   